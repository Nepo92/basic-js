  abc = [];

  init() {
    for (let i = 'A'.charCodeAt(0); i <= 'Z'.charCodeAt(0); i++) {
      this.abc.push(String.fromCodePoint(i).toUpperCase());
    }
  }

  encrypt(message, chipherKey) {
    if (!message || !chipherKey) throw new Error('Incorrect arguments!');

    const upperKey = chipherKey.toUpperCase();
    const key = upperKey.replace(/\s/g, '');

    const upperMessage = message.toUpperCase();
    const msg = upperMessage.trim();

    const result = [];

    const keyLength = key.length;
    const msgLength = msg.length;

    for (let i = 0, n = 0; i < msgLength; i++, n++) {
      const current = msg[i];

      if (!~this.abc.indexOf(current)) {
        result.push(current);
        n--;
        continue;
      }

      const code = current.charCodeAt(0);

      const row = key.charCodeAt(n % keyLength) - 65;
      const column = code - 65;

      result.push(this.tabula_recta[row][column])
    }

    if (this.noReverse) {
      return result.join('');
    } else {
      return result.reverse().join('');
    }
  }

  decrypt(encryptedMessage, chipherKey) {
    if (!encryptedMessage || !chipherKey) throw new Error('Incorrect arguments!');

    const upperKey = chipherKey.toUpperCase();
    const key = upperKey.replace(/\s/g, '');

    const upperMessage = encryptedMessage.toUpperCase();
    const msg = upperMessage.trim();

    const result = [];

    const keyLength = key.length;
    const msgLength = msg.length;

    for (let i = 0, n = 0; i < msgLength; i++, n++) {
      const current = msg[i];

      const rowIndex = key.charCodeAt(n % keyLength) - 65;
      const row = this.tabula_recta[rowIndex];

      if (!this.abc.includes(current)) {
        result.push(current);
        n--;
        continue;
      }

      const columnIndex = row.indexOf(current);
      result.push(this.tabula_recta[0][columnIndex]);
    }

    if (this.noReverse) {
      return result.join('');
    } else {
      return result.reverse().join('');
    }
  }
